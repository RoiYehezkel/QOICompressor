package Compressor;

public class QOIHeaderException extends Exception {
	public QOIHeaderException() {
		super();
	}

	public QOIHeaderException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public QOIHeaderException(String message, Throwable cause) {
		super(message, cause);
	}

	public QOIHeaderException(String message) {
		super(message);
	}

	public QOIHeaderException(Throwable cause) {
		super(cause);
	}
}
