public class QOIException extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public QOIException() {
		super();
	}

	public QOIException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public QOIException(String message, Throwable cause) {
		super(message, cause);
	}

	public QOIException(String message) {
		super(message);
	}

	public QOIException(Throwable cause) {
		super(cause);
	}
}
